package class2;

public class Variables3 {
    public static void main(String[] args) {

        //VERY NICE TIP: use "ctrl alt L" to format code nicely
        //ALSO NICE TIP: use "ctrl d" to duplicate a line of code

        //if we want to store decimal numbers in memory, we use the following data types
        //1) float 2) double
        //similar to the long data type, float numbers need an "f" at the end

        //float range is 6 to 7 decimal points
        float myNumBox=10.5f;
        float mySecondBox=5.5f;

        //double range is 15 decimal pts
        double largeDecimalBox=1232879.28712978;
        System.out.println(myNumBox);

        //char is used to hold one letter/symbol at a time
        //use single quotes to assign
        char letter='o';
        char symbol='$';

        //string is used to hold multiple letters/symbols
        //use double quotes to assign
        String name="moo";
        System.out.println(name);

        //boolean is used to hold true/false data

        boolean hungry= true;
        boolean happy=false;
    }
}
