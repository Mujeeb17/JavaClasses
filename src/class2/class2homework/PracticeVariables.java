/*
TASK 1: Create a Java program (A class with main method) and name it Practice Variables
In your program create variables to store all different types of primitive data to test the range
Print the value of each variable
*/
public class PracticeVariables {
    public static void main(String[] args) {

        //declaring & assigning primitive data types
        byte num1 = 127;
        short num2 = 32767;
        int num3 = 2147483647;
        long num4 = 9223372036854775807l;
        float num5 = 10.1234567f;
        double num6 = 100.123456789123456789;
        char symbol = '#';
        boolean val1 = true;

        //printing each variable
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);
        System.out.println(num6);
        System.out.println(symbol);
        System.out.println(val1);


    }//end of main method

}//end of class
