package class3.class3homework;

/*
TASK 1: declare 2 double values and if value of 1st variable
is higher than the 2nd, print "double value is larger than "
 */
public class DoubleComparison {
    public static void main(String[] args) {

        double x = 20.5;
        double y = 10.5;

        if (x > y) {
            System.out.println("double value (x) is larger than (y)");
        } else {
            System.out.println("double value (x) is smaller than (y)");
        }
    }
}
