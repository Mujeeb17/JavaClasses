package class3IfElseTypeCasting;

public class ShortHandOperator {
    public static void main(String[] args) {

        int num = 10;

        num = num + 10;
        System.out.println(num);

        //shortcut for the above statement
        num += 10;
        System.out.println(num);

        num *= 2;
        System.out.println(num);

        num -= 10;
        System.out.println(num);

        num /= 10;
        System.out.println(num);
    }
}
